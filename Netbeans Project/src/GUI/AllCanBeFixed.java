    private void saveChangesBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveChangesBtnActionPerformed
        if("".equals(this.IdNumberTxtField.getText()) || "".equals(this.emailTxtField.getText()) || "".equals(this.addressTxtField.getText()) || "".equals(this.DoBTxtField.getText()) ||
            "".equals(this.NameTxtField.getText()) || "".equals(this.phoneNumberTxtField.getText()) || "".equals(this.workHoursTxtField.getText()))
        {
            JOptionPane.showMessageDialog(rootPane, "Fill all the fields with the required info");
        }
        else if(Paysheet.getEmployee(this.IdNumberTxtField.getText())==null)
        {
            JOptionPane.showMessageDialog(rootPane, "There is no employee registered with that ID number");
            clearFields();
            this.saveChangesBtn.setEnabled(false);
            this.extraHoursTxtField.setEnabled(false);
            this.AddExtraHoursBTN.setEnabled(false);
        }
        else
        {
            Paysheet.modifyEmployee(this.NameTxtField.getText(),this.DoBTxtField.getText(), this.genderComboBox.getSelectedItem().toString(),
                this.phoneNumberTxtField.getText(),this.emailTxtField.getText(),this.addressTxtField.getText(),this.IdNumberTxtField.getText(),
                this.paymentTypeComboBox.getSelectedItem().toString(),Integer.parseInt(this.PPHtxtField.getText()),Integer.parseInt(this.workHoursTxtField.getText()));
            clearFields();
            this.saveChangesBtn.setEnabled(false);
            this.extraHoursTxtField.setEnabled(false);
            this.AddExtraHoursBTN.setEnabled(false);
            JOptionPane.showMessageDialog(rootPane, "Employee modified correctly");
        }
    }//GEN-LAST:event_saveChangesBtnActionPerformed

