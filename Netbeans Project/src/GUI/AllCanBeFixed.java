    private void btnMostrarInfoEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarInfoEmpleadoActionPerformed
        Employee mEmployee=Paysheet.getEmployee(this.IdNumberTxtField.getText());
        if (mEmployee==null)
        {
            JOptionPane.showMessageDialog(rootPane, "There is no employee registered with that ID number");
            clearFields();
            this.saveChangesBtn.setEnabled(false);
            this.extraHoursTxtField.setEnabled(false);
            this.AddExtraHoursBTN.setEnabled(false);
        }
        else
        {
            if(this.optionComboBox.getSelectedItem()=="Show")
            {
                this.NameTxtField.setText(mEmployee.getName());
                this.addressTxtField.setText(mEmployee.getAddress());
                this.emailTxtField.setText(mEmployee.getEmail());
                this.DoBTxtField.setText(mEmployee.getDateOfBirth());
                this.phoneNumberTxtField.setText(mEmployee.getPhoneNumber());
                this.PPHtxtField.setText(Integer.toString(mEmployee.getPaymentPerHour()));
                this.genderComboBox.setSelectedItem(mEmployee.getGender());
                this.paymentTypeComboBox.setSelectedItem(mEmployee.getPaymentType());
                this.workHoursTxtField.setText(Integer.toString(mEmployee.getWorkHours()));
                this.saveChangesBtn.setEnabled(true);
                this.extraHoursTxtField.setEnabled(true);
                this.AddExtraHoursBTN.setEnabled(true);
                if(mEmployee instanceof Cashier)
                {
                    this.RoleCombobox.setSelectedItem("Cashier");
                }
                else if(mEmployee instanceof Dispenser)
                {
                    this.RoleCombobox.setSelectedItem("Dispenser");
                }
                else if(mEmployee instanceof Maintainer)
                {
                    this.RoleCombobox.setSelectedItem("Maintainer");
                }
                else if(mEmployee instanceof Manager)
                {
                    this.RoleCombobox.setSelectedItem("Manager");
                }
            }
            if(this.optionComboBox.getSelectedItem()=="Delete")
            {
                Paysheet.deleteEmployee(this.IdNumberTxtField.getText());
                clearFields();
                chargeComboBoxEmployees();
                this.saveChangesBtn.setEnabled(false);
                this.extraHoursTxtField.setEnabled(false);
                this.AddExtraHoursBTN.setEnabled(false);
                JOptionPane.showMessageDialog(rootPane, "Employee deleted");
            }
        }
    }//GEN-LAST:event_btnMostrarInfoEmpleadoActionPerformed

