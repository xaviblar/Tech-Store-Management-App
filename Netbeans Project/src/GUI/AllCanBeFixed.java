    private void chargeComboBoxArticles()
    {
        articles mArticle;
        DefaultComboBoxModel ModesCBM = new DefaultComboBoxModel(); 
        DefaultComboBoxModel ModesCBM2 = new DefaultComboBoxModel(); 
        for(int i=0;i<listArticles.list.size();i++)
        {
            mArticle=listArticles.list.get(i);
            if(mArticle instanceof toSellArticles)
            {
               ModesCBM.addElement(mArticle.getCode()); 
            }
            else if(mArticle instanceof toFixArticles && mArticle.isFixed()==false)
            {
               ModesCBM2.addElement(mArticle.getCode());
            }
        }
        this.comboBoxArticles.setModel(ModesCBM);
        this.comboBoxPendingRepairs.setModel(ModesCBM2);
    }
    
