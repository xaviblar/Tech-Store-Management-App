    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Bill mBill=listBills.search(this.txtFieldSaleID.getText());
        if (mBill==null)
        {
            JOptionPane.showMessageDialog(rootPane, "There is no sale registered with that ID number");
            clearFields();
            this.btnAddArticleSale.setEnabled(false);
            this.txtFieldAmountArticle.setEnabled(false);
        }
        else
        {
            if(this.comboBoxOptionSales.getSelectedItem()=="Show")
            {
                this.txtFieldClientNameSale.setText(mBill.getClientName());
                this.comboBoxArticles.setSelectedItem(mBill.getIDEmployee());
                this.btnAddArticleSale.setEnabled(true);
                this.txtFieldAmountArticle.setEnabled(true);
            }
            else if(this.comboBoxOptionSales.getSelectedItem()=="Delete")
            {
                for(int i=0;i<mBill.getList().size();i++)
                {
                    ((toSellArticles)listArticles.search(mBill.getList().get(i).getCode())).increaseStock(1);
                }
                listBills.deleteBill(this.txtFieldSaleID.getText());
                clearFields();
                this.btnAddArticleSale.setEnabled(false);
                this.txtFieldAmountArticle.setEnabled(false);
                JOptionPane.showMessageDialog(rootPane, "Sale deleted");
                chargeComboBoxBills();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

